func serveRun(cmd *cobra.Command, args []string) {
	if len(args) == 0 {
		fmt.Println("filename not specified")
		return
	}

	filename = args[0]
	http.HandleFunc("/", serveHome)
	http.HandleFunc("/ws", serveWs)
	if err := http.ListenAndServe(*addr, nil); err != nil {
		log.Fatal(err)
	}
}

